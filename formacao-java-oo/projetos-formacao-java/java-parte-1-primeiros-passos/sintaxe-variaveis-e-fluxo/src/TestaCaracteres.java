public class TestaCaracteres {
	public static void main(String[] args) {
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor + 1);
		System.out.println(valor);
		
		String palavra = "Vou jogar pokemon hoje!!!";
		System.out.println(palavra);
		
		palavra = palavra + 20;
		System.out.println(palavra);
	}
}
